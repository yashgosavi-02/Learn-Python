name = "Harry"
print(name)
print(type(name))
print(name[0:4])  # slicing (from 0 to n-1)
print(name.capitalize())  # first letter capital
print(name.upper())  # to upper case
print(name.lower())  # to lower case
print(name.count("r"))  # count occurrence of provided substring
print(name.isalnum())  # check string is alphanumeric

# Strings are immutable (it cannot be changed)
